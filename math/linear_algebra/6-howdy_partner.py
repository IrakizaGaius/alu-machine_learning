def cat_arrays(arr1, arr2):
    """
    Concatenates two arrays.
    Args:
        arr1: A list representing an array.
        arr2: A list representing an array.
    Returns:
        A new list representing the concatenation of the arrays.
    """
    return arr1 + arr2
